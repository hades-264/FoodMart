
-- Grocery Management System
-- Schema Name: GMSAPP

-- Table: USERS
-- Stores information for both Customers and Admin.
-- ROLE distinguishes between 'CUSTOMER' and 'ADMIN'.

CREATE SCHEMA GMSAPP;

CREATE TABLE GMSAPP.USERS (
    USER_ID VARCHAR(50) NOT NULL PRIMARY KEY,
    USER_NAME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL,
    ADDRESS VARCHAR(255),
    CONTACT_NUMBER VARCHAR(15),   
    ROLE VARCHAR(10) NOT NULL CONSTRAINT ROLE_TYPE_CHECK CHECK (ROLE IN ('CUSTOMER', 'ADMIN'))
);

-- -------------------------------------------------
-- Table: PRODUCTS
-- Stores details about grocery products.
-- IMAGE_FILE_NAME stores the name of the product's image file (e.g., '3-4521-7384-9.jpg').
-- -------------------------------------------------
CREATE TABLE GMSAPP.PRODUCTS (
    PRODUCT_ID VARCHAR(50) NOT NULL PRIMARY KEY,
    PRODUCT_NAME VARCHAR(100) NOT NULL,
    PRODUCT_DESCRIPTION VARCHAR(255),
    PRICE DECIMAL(10, 2) NOT NULL,       
    AVAILABLE_QUANTITY INT NOT NULL DEFAULT 0,
    CATEGORY VARCHAR(50),
    IS_AVAILABLE BOOLEAN DEFAULT TRUE NOT NULL,
    IMAGE_FILE_NAME VARCHAR(100)            -- e.g., '3-4521-7384-9.jpg', 'placeholder.png'
);

-- -------------------------------------------------
-- Table: ORDERS
-- Stores information about customer purchases.
-- -------------------------------------------------
CREATE TABLE GMSAPP.ORDERS (
    ORDER_ID VARCHAR(50) NOT NULL PRIMARY KEY,
    USER_ID VARCHAR(50) NOT NULL,               
    ORDER_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP, 
    TOTAL_AMOUNT DECIMAL(10, 2) NOT NULL,      
    STATUS VARCHAR(20) DEFAULT 'Pending' CONSTRAINT STATUS_CHECK CHECK (STATUS IN ('Pending', 'Shipped', 'Delivered', 'Cancelled')),
    SHIPPING_ADDRESS VARCHAR(255),             
    CONTACT_ON_ORDER VARCHAR(15),      
    FOREIGN KEY (USER_ID) REFERENCES GMSAPP.USERS(USER_ID)
);

-- -------------------------------------------------
-- Table: ORDER_ITEMS
-- A table linking products to orders, storing quantity and price at time of purchase.
-- -------------------------------------------------
CREATE TABLE GMSAPP.ORDER_ITEMS (
    ORDER_ITEM_ID INT NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
    ORDER_ID VARCHAR(50) NOT NULL,          
    PRODUCT_ID VARCHAR(50) NOT NULL,
    PRODUCT_NAME VARCHAR(100) NOT NULL,
    QUANTITY INT NOT NULL,
    PRICE_AT_PURCHASE DECIMAL(10, 2) NOT NULL, 
    FOREIGN KEY (ORDER_ID) REFERENCES GMSAPP.ORDERS(ORDER_ID) ON DELETE CASCADE, 
    FOREIGN KEY (PRODUCT_ID) REFERENCES GMSAPP.PRODUCTS(PRODUCT_ID))

